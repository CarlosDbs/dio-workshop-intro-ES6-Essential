//Igual (==)
//Retorna verdadeiro caso os operandos sejam iguais. 3 == var1

"3" == var_1
3 == '3'//true

//Não igual ou diferente
//Retorna verdadeiro caso os operandos não sejam iguais var_1 != 4
var_2 != "3"

//Estritamente igual ===
//Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo.
3 === var_1

//Estritamente não igual !==
//Retorna verdadeiro caso os operandos nao sejam iguais e/ou nao sejam do mesmo tipo
var_1 !== "3"
3 !== '3'

//Maior que >
//Retorna verdadeiro caso o operando da esquerda seja maior que o da direita
var_2 > var_1
"12" > 2

//Maior que ou igual >=
//Retorna verdadeiro caso o operando da esquerda seja maior ou igual ao da direita
var_2 >= var_1
var_1 >= 3

//Menor que <
//Retorna verdadeiro caso o operando da esquerda seja menor que o da direita
var_1 < var_2
"12" < "2"

//Menor que ou igual <=
//Retorna verdadeiro caso o operando da esquerda seja menor ou igual ao da direita
var_1 <= var_2
var_2 <= 5
